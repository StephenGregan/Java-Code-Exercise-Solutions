package sheet1VariableOperators;

public class Ex1PrintLines {

	public static void main(String[] args) {
		// Prints out "hello" with newline
		System.out.println("hello");
		// Prints out "hello" with newline
		System.out.println("hello");
		// \n\n gives two newlines after "hello"
		System.out.print("hello\n\n");
		// Prints out "tab      tab      "
		System.out.println("tab \t tab \t");

	}

}
